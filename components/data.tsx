export const renderUploadedFile = (uploadBlobs: string[], format: string, index: number, ext?: any) => {
  if (!uploadBlobs[index]) return null;

  if (format === "Video") {
    return (
      <video
        src={uploadBlobs[index]}
        controls
        className="w-full h-full object-cover rounded-lg"
      />
    );
  }

  if (format === "Slideshow") {
    // Check if the file is a PDF based on the blob URL or ext
    const isPDF = ext?.name?.toLowerCase().endsWith(".pdf") || uploadBlobs[index].includes("application/pdf");

    if (isPDF) {
      return (
        <iframe
          src={uploadBlobs[index]}
          className="w-full h-full rounded-lg"
          title={`Slideshow ${index}`}
        />
      );
    }

    // Handle other slideshow formats (e.g., PPTX)
    return (
      <>
        {typeof uploadBlobs[index] === "string" && ext?.name?.includes("pptx") ? (
          <iframe
            src={`https://view.officeapps.live.com/op/embed.aspx?src=${encodeURIComponent(
              uploadBlobs[index]
            )}`}
            className="w-full h-full rounded-lg"
            title={`Slideshow ${index}`}
          />
        ) : (
          <iframe
            src={uploadBlobs[index]}
            className="w-full h-full rounded-lg"
            title={`Slideshow ${index}`}
          />
        )}
      </>
    );
  }

  return (
    <Image
      src={uploadBlobs[index]}
      alt={`Image ${index}`}
      className="w-full h-full object-cover rounded-lg"
      width={225}
      height={105}
    />
  );
};

export function hasFormatEntered(channelMix) {
  // Loop through each funnel stage
  for (const stage of channelMix) {
    // Check each type of media in the funnel stage
    const mediaTypes = [
      "social_media",
      "display_networks",
      "search_engines",
      "streaming",
      "ooh",
      "broadcast",
      "messaging",
      "print",
      "e_commerce",
      "in_game",
      "mobile",
    ];

    // Loop through each media type
    for (const mediaType of mediaTypes) {
      const platforms = stage[mediaType];

      // Check each platform for a non-empty 'format' array
      for (const platform of platforms) {
        if (platform.format && platform.format.length > 0) {
          return true; // At least one format is entered
        }
      }
    }
  }
  return false; // No format found for any platform
}



